        <p style="font-size: 20px; line-height:1.5;">Create a list with your child of all the items you can imagine seeing on your visit. Then 
        create your own bingo boards on paper or cardboard, drawing in the items or writing by name. For example, your bingo board could include 
        items such as a chipmunk, woodpecker, butterfly, clover, pinecone, muddy puddle, dog, and more. Remember to make all the boards different 
        so no two are alike. Mark the items off with a pen or sticker as you come across them on your visit, getting ‚ÄúBingo‚Äù on marked rows or 
        entire boards.</p>
        <p style="font-size: 20px; line-height:1.5;">If you have a very young child with a wild imagination, try incorporating an unlikely or 
        mythical item on the board, making sure to only incorporate one per board. (Or go ahead and hide a sparkled unicorn figurine behind a bush!)</p>
