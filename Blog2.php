      <h2 style="padding-top:2%; padding-bottom:1.75%; color:#367E18; font-weight:bold;" class="text-center">Leaf Art</h2>
    <div style = "margin: auto; width: 70%;padding-left:13.5%;">
        <img src="images/leaf-art.jpg" alt="Leaf Art" width="79.5%" height="">
        <div style="padding-right:20%">
        <p style="font-size: 20px; line-height:1.5;"><em>Leaf prints: Watercolors can work as well. Try other natural items, such as pinecones 
            and dandelions, for more unique painting patterns.</em></p>
        <p style="font-size: 20px; line-height:1.5;">These activities will happen at home after your visit. While on your outing, 
        collect several types of leaves for leaf-art projects. There are likely more than a thousand crafts to do with leaves, below are a few 
        ideas to get started. All you will need are leaves, paper, paint, tape, crayons, and glue to do all three projects.</p>
        <p style="font-size: 20px; line-height:1.5;">üü¢<strong>Painted Leaf Prints:</strong> With paint, paint a leaf with a brush or dip 
        it in a paint covered plate. Then push the leaf paint-side down onto craft paper or another intended surface. Keep going with more leaves 
        and colors.</p>
        <p style="font-size: 20px; line-height:1.5;">üü¢<strong>Crayon Leaf Rubbing:</strong> On a table, tape a few leaves to a blank sheet of 
        white paper. Then turn the paper over so the leaf side is against the table. Then shade the hidden leaf with the side of a paper-free crayon.</p>
        <p style="font-size: 20px; line-height:1.5;">üü¢<strong>Leaf Collage:</strong> Glue leaves or other found parts of trees (e.g., twigs, seed 
        pods) to make unique collages. Design animals, houses, or whatever appeals to your little one in the moment.</p>
