      <h2 style="padding-top:2%; padding-bottom:1.75%; color:#367E18; font-weight:bold;" class="text-center">Take a Sensory Stroll</h2>
    <div style = "margin: auto; width: 70%;padding-left:13.5%;">
        <img src="images/sensory-stroll.jpg" alt="Sensory Stroll" width="79.5%" height="">
        <div style="padding-right:20%">
        <p style="font-size: 20px; line-height:1.5;">This activity uses our senses and encourages kids to notice and connect with their 
        surroundings. Before you begin, talk about what you might see, hear, smell, and touch (if there will be safe foraging or snacks, add in taste!)
        on your sensory stroll.</p>
        <p style="font-size: 20px; line-height:1.5;">Once you've reached the destination, start by drawing the child's attention to the ground. How does 
        it feel under them? Reach down and touch it if you need to. Is it rough and hot, or soft and cool? Then focus on hearing, listening for different 
        sounds, near and far. Continue with smell and sight, pausing every now and then to find new items to sense on your journey. Let the child set the pace.</p>
        <p style="font-size: 20px; line-height:1.5;">For very young children, it may be best to focus on one sense, such as hearing. Encourage them to mimic what they hear.</p>
        </div>
