      <h2 style="padding-top:2%; padding-bottom:1.75%; color:#367E18; font-weight:bold;" class="text-center">Observation Journal</h2>
    <div style = "margin: auto; width: 70%;padding-left:13.5%;">
        <img src="images/observatory.jpg" alt="Observation Journal" width="79.5%" height="">
        <div style="padding-right:20%">
        <p style="font-size: 20px; line-height:1.5;">Bring sketchpads and colored pencils or crayons on your outing (a magnifying glass may also be
         helpful) and ask kids to draw or write about the natural items they observe. You can start right away or on a break once they've spotted 
         something interesting. If possible, break down the item further by inspecting it up close, such as helping to find and draw the different 
         parts of a flower.</p>
        <p style="font-size: 20px; line-height:1.5;">Sensory activities like this one and the stroll above can help increase awareness and focus. 
        To engage in this activity more at home, learn about the items you and the children observed online or in a book and talk about them 
        together. For instance, if a child drew a plant, learn its name, range, and what eats it.</p>
        </div>
